// コンストラクタ
//＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝
Goal::Goal(std::string keyName, size_t setNumber)
{
	tagName				 = keyName;
	idNumber			 = setNumber;
	modelPasses[keyName] = passName + keyName;

	Collision::registerList(&myTransform, keyName);
	
	isUsed = false;
}

//＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝
// デストラクタ
