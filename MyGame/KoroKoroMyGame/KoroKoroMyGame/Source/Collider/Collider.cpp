// コライダー更新
//＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝
void Collider::UpdateCollider(D3DXMATRIX SetMtx,D3DXCOLOR SetColor)
{
	mtxWorld = SetMtx;
	figurePtr->UpdateFigure(mtxWorld,SetColor);
}

//＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝
