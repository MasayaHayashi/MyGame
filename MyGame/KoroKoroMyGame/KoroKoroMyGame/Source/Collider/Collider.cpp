void Collider::InitCollider(D3DXVECTOR3 Pos,D3DXVECTOR3 CollisionSize,D3DXVECTOR3 CenterPos)
{
	figurePtr->InitFigure(Pos,CollisionSize,CenterPos);
}

//＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝
