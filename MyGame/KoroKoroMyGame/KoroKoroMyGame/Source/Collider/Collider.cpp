//＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝
void Collider::DrawCollider()
{
	// デバイス取得
	LPDIRECT3DDEVICE9 pDevice = DirectX3D::getDevice();

	// 描画
	figurePtr->DrawFigure();

}

