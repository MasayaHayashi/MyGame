// コンストラクタ
// Pos  : 位置
// Size : 大きさ
// 位置、サイズ指定
//＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝
Collider::Collider(D3DXVECTOR3 setPos ,D3DXVECTOR3 setSize)
{
	// オブジェクト初期化
	figurePtr.reset(new Figure());

	// 位置、サイズ初期化
	Pos   = setPos;
	Size  = setSize;
}


//＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝
