SelectManager::SelectManager()
{
	for (UINT playerindex = 0; playerindex < MaxPlayer; playerindex ++)
	{
		currentSelectModels[playerindex] = playerindex;
	}
}

//＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝
