//＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝
// インデックス取得
//＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝
UINT SelectManager::getModelIgndex(UINT playerIndex)
{
	if (playerIndex < currentSelectModels.size())
	{
		return -1;
	}

	return currentSelectModels[playerIndex];
}